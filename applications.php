<li><strong>Prototypes:</strong> No tooling or set-up charges for most designs submitted with CAD files</li>
<li><strong>Production Quantities:</strong> Maximum material usage; Minimum kerf loss, efficient nesting</li>
<li><strong>Thick Materials:</strong> Capable of cutting eight inches thick</li>
<li><strong>Heat Sensitive Material:</strong> Abrasive waterjet cutting is a cold process and will not affect the temper of the material</li>
<li><strong>Hard to machine material:</strong> Abrasive waterjet cutting is different than traditional machining and is not fazed by material that is tough or gummy, that galls or workhardens</li>
<li><strong>Composites:</strong> Materials with different machining properties can be cut together</li>
<li><strong>Art Work:</strong> We can work directly from computer generated artwork or scan your drawings and sketches.  Let your imagination run wild! If you can draw it, we can cut it. Any 2-D design, any material, any thickness of sheet or plate.  Because it is a cold cutting process cleanup is minimal, the edges are not burned, the part is not warped.</li>
<li><strong>Signs:</strong> Any lettering style and logo design can be cut in any material.</li>
<li><strong>Architectural:</strong> Waterjet cutting opens your design horizons. You are not limited to bars and stock shapes. You can create, quickly and inexpensively, designs that would be very expensive or impossible to create with any other process.  You are not limited in material choices: Brass, copper, bronze, aluminum, stainless steel and mild steel can all be cut. Even with conventional designs the advantages are many: welding and grinding are eliminated. There are no joints that you have to worry about rusting.  So you save time and money, and deliver a unique product at a reasonable cost.</li>
<li><strong>Machine Parts:</strong> Waterjet cutting saves you time and money, cutting near net or final shapes.  Abrasive Waterjet can cut near-net or final shapes, including holes, out of any material, any thickness, without creating a heat affected zone (haz).</li>
