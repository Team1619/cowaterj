<p>The abrasive water jet process works by cutting metal or other materials using a thin stream of highly 
pressurized water. This stream is directed by a computer numerical controlled (CNC) arm, achieving a 
precision of ±0.005 inches per side. Colorado WaterJet machines feature an actuating head which can 
control the stream further and achieve a 90 degree edge, called dynamic cutting. Parts cut with water 
jet technology are ready to be machined with normal processes and lack the difficult heat affected 
zones that come with other cutting processes, such as plasma or laser.</p>

